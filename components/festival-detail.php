      <li>Một người cha nghèo không có tiền cho lễ cưới ba cô con gái.</li>
      <li>Saint Nicholas (tiền thân của Santa Claus) lén thả vàng qua cửa sổ để giúp gia đình.</li>
      <li>Vàng rơi vào tất hoặc giày của các cô gái đang phơi gần lò sưởi.</li>
      <li>Từ đó, người ta bắt đầu treo tất để Santa bỏ quà vào, thay vì trực tiếp đặt trên bàn hay dưới cây.</li>
    </ul>
  </li>
  <br>
  <li>Trong lễ Yule, người Bắc Âu ăn uống, nhảy múa, uống rượu, dâng lễ vật cho thần mặt trời, thần gia súc. Theo đó trong Christmas người ta cũng tổ chức tiệc gia đình, quà cáp, một phần là kế thừa tinh thần hội hè Yule, nhưng thay thần bằng Chúa và các thánh.</li>
