  Thừa hưởng từ truyền thống <b>cây xanh bất tử</b> của Yule. Christmas cũng sử dụng cây thông trang trí thêm đèn và quả cầu làm biểu tượng.</li>
  <br>
  <li>Những chiếc <b>chuông</b> thường được ngân vang trong dịp Đông chí để xua đuổi ma quỷ trỗi dậy giữa những ngày tối tăm của năm. Tiếng <b>chuông</b> vang lên vào buổi sớm, khi mọi người thức dậy, nhằm xua tan bóng đêm và đón chào những ngày ấm áp, rực rỡ hơn sau Đông chí.</li>
