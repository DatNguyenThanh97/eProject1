  <br>
  <li><b>Bánh gừng</b> (gingerbread) được xem là một loại bánh đặc biệt trong giai đoạn này, vì gừng chỉ xuất hiện ở châu Âu sau khi các cuộc Thập tự chinh mang về vào thế kỷ XI.
  <div style="display: flex; justify-content: center; margin: 10px 0;">
