  Thời đó có những quy định nghiêm ngặt đối với các loại bánh đặc sản, nên <b>bánh gừng</b> chỉ được phép làm vào dịp lễ hội. Chính vì vậy, nó dần gắn liền với mùa đông và lễ Yule.</li>
  <br>
  <li><b>Tất Giáng Sinh</b> (Christmas stocking) cũng có nguồn gốc truyền thuyết + phong tục dân gian châu Âu, đặc biệt là từ Saint Nicholas / Hà Lan / Đức. Câu chuyện phổ biến nhất là:
